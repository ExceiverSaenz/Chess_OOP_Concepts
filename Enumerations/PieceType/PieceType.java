package Enumerations.PieceType;

/*
Author: Cheng Bungein
The enumAtributes class is an enum in Java that represents attributes of a chess set.
It includes constants for the chess pieces (PAWN, ROOK, KNIGHT, BISHOP, QUEEN, KING),
*/
/**
 * Enumerations for the chess pieces.
 */
public enum PieceType {
    /**
     * Represents a pawn piece.
     */
    PAWN,
    /**
     * Represents a rook piece.
     */
    ROOK,
    /**
     * Represents a knight piece.
     */
    KNIGHT,
    /**
     * Represents a bishop piece.
     */
    BISHOP,
    /**
     * Represents a queen piece.
     */
    QUEEN,
    /**
     * Represents a king piece.
     */
    KING
}
